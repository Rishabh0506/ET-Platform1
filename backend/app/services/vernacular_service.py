_FALLBACK = "This article is currently available in English only. Vernacular support coming soon."


def generate_vernacular(article: dict, language: str) -> str:
    """
    Return a culturally adapted, simplified explanation of the article in the given language.

    Args:
        article: dict with at least a 'category' key.
        language: one of 'Hindi', 'Tamil', 'Telugu', 'Bengali'.

    Returns:
        A simplified, native-language explanation string.
    """
    lang_map = _VERNACULAR_MATRIX.get(language)
    if not lang_map:
        return _FALLBACK

    category = article.get("category", "")
    return lang_map.get(category, lang_map["default"])
